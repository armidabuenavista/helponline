	<h3>Pregnancy</h3>
			<hr class="colorgraph"></hr>
			
			
			
			
			<p style=" text-align: justify;">Tips to attain a healthy weight during pregnancy<br><br>

1.	Maintain a normal weight using body mass index (BMI)  even before pregnancy. A healthy adult should have a BMI between  18.5 - 24.9. Normal weight before pregnancy ensures that the mother has adequate nutrient stores  to nourish the baby in the early part of pregnancy.   <br>
2.	Monitor weight  gain especially during the second and third trimesters of pregnancy .  Low weight gain may predispose the infant to be born low birthweight  or small for gestational age; excess weight gain predisposes the infant to have high birthweight that can lead to obesity and other related conditions. (See table on  recommended weight gain of IOM).  <br>
3.	Eat a balanced meal containing Go, Grow and Glow foods (See PP for pregnant). A balanced  meal should provide all the necessary nutrients needed to support the needs of pregnancy. <br>
4.	Increase intake of protein foods to promote the growth of the baby. Fish, meat, poultry, eggs and milk provide high quality protein as well as legumes/beans with fish/meat combination.   <br> 
5.	Avoid skipping meals. Take your meals and snacks at regular time intervals to avoid discomfort due to increased food volume in the stomach.  <br>
6.	Take enough fruits and vegetables to provide your body with essential  vitamins, minerals and fiber in the diet.  <br>
7.	Avoid eating salty foods. Salt contains sodium which increases retention of water in the body . <br>
8.	Drink enough water for adequate hydration. Pregnant women need more water to have an efficient delivery of nutrients to both mother and baby.  Adequate water intake also helps prevent constipation, fatigue, urinary tract infection that are common in pregnancy.  <br>
9.	Healthy pregnant women should engage in physical activity to prepare for labor and childbirth.  About 150 minutes of moderate to vigorous activity is recommended per week.  Common physical activities that can be done by pregnant women are walking, dancing, swimming, aerobics, stretching and strengthening exercises.

 </p> 
 
 
 <p style=" text-align: justify;">Other recommendations for a healthy pregnancy<br><br>

1.	Do not smoke or drink during pregnancy. The toxic chemicals from cigarettes such as nicotine and carbon monoxide is very harmful to the baby. Smoking during pregnancy can result to low birthweight, stillbirth and premature birth. Alcohol drinking leads to fetal alcohol syndrome which is characterized by physical, mental or learning behaviors of the baby.<br>
2.	Avoid stress and maintain a relaxed disposition. Stress increases blood pressure that may lead to hypertension during pregnancy. Long-time stress can affect the fetus in terms of being born stillbirth or premature and may also lead to behavioral problems during childhood. <br>   
3.	Visit the health center regularly to check  your health and the baby. Regular check up helps to monitor status of pregnancy and the health of the infant while in the womb. Regular check up also allows an opportunity for the mother to be given appropriate intervention  if there is any  problem in the course of pregnancy.

 </p> 
 
 
 
